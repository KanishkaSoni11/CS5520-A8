package com.cs5520.cs5520_a8;

public class SentHistory {

    private int sticker;
    private int count;

    public SentHistory(int sticker, int count) {
        this.sticker = sticker;
        this.count = count;
    }

    public int getCount() {
        return count;
    }

    public int getSticker() {
        return sticker;
    }
}
